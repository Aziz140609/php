#aritmatika

A = 12+3
B = 12-3
C = 12*3
D = 12/3
E = 12%5
F = 12//5
G = 12**3
print("Hasil dari 12 + 3 =", A)
print("Hasil dari 12 - 3 =", B)
print("Hasil dari 12 * 3 =", C)
print("Hasil dari 12 / 3 =", D)
print("Hasil dari 12 % 5 =", E)
print("Hasil dari 12 // 5 =", F)
print("Hasil dari 12 ** 3 =", G)

#operator pembanding
a = 10
b = 12
print("Apakah a lebih besar dari b =", a > b)
print("Apakah a lebih kecil dari b =", a < b)
print("Apakah a sama dengan b =", a == b)
print("Apakah a tidak sama dengan b =", a != b)
print("Apakah a lebih besar atau sama dengan b =", a >= b)
print("Apakah a lebih kecil atau sama dengan b =", a <= b)

#operator logika
x = True
y = False
print("Hasil dari x and y =", x and y)#harus kedua2nya true
print("Hasil dari x or y =", x or y)#cukup salah satu true
print("Hasil dari not x =", not x)#membalik nilai

#operator identity
m = ["apel", "jeruk"]
n = m
print("Apakah m is n =", m is n)#true karena menunjuk ke object yg sama
print("Apakah m is not n =", m is not n)#false karena menunjuk ke object yg sama

#operator keanggotaan
buah = ["apel", "jeruk", "mangga"]
print("Apakah 'apel' ada di dalam list buah =", "apel" in buah)
print("Apakah 'pisang' tidak ada di dalam list buah =", "pisang" not in buah)

